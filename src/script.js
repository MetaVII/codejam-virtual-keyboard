(function setInitialLang() {
  const lang = sessionStorage.getItem('lang');
  if (!lang) {
    sessionStorage.setItem('lang', 'eng');
  }
})();

function appendElement(container, className) {
  const ELEMENT = document.createElement('div');
  ELEMENT.classList.add(...className.split(' '));
  container.append(ELEMENT);
  return ELEMENT;
}

const TEXTAREA = document.createElement('textarea');
TEXTAREA.classList.add('textarea');
document.body.prepend(TEXTAREA);

const CONTAINER = document.createElement('div');
CONTAINER.classList.add('keyboard__container');
document.body.append(CONTAINER);

const LEGEND = document.createElement('p');
LEGEND.classList.add('legend');
LEGEND.innerHTML =
  'Клавиатура создана в операционной системе Windows. Переключение языка ctrl + alt';
document.body.append(LEGEND);

let isCapsActive = false;

// print button inside textarea
function printButton(text, code) {
  if (
    code === 'CapsLock' ||
    code === 'ShiftLeft' ||
    code === 'ShiftRight' ||
    code === 'ControlLeft' ||
    code === 'ControlRight' ||
    code === 'MetaLeft' ||
    code === 'AltLeft' ||
    code === 'AltRight'
  ) {
    return;
  }

  const { selectionStart, selectionEnd } = TEXTAREA;

  if (code === 'ArrowLeft') {
    if (selectionStart > 0) {
      TEXTAREA.selectionStart -= 1;
      TEXTAREA.selectionEnd -= 1;
    }
    return;
  }

  if (code === 'ArrowRight') {
    TEXTAREA.selectionEnd += 1;
    TEXTAREA.selectionStart += 1;
    return;
  }

  if (code === 'Backspace' || code === 'Delete') {
    if (selectionStart !== selectionEnd) {
      TEXTAREA.setRangeText('', selectionStart, selectionEnd, 'end');
    } else if (code === 'Backspace' && selectionStart > 0) {
      TEXTAREA.setRangeText('', selectionStart - 1, selectionEnd, 'end');
    } else if (code === 'Delete') {
      TEXTAREA.setRangeText('', selectionStart, selectionEnd + 1, 'end');
    }
    return;
  }

  if (code === 'Enter') {
    TEXTAREA.setRangeText('\n', selectionStart, selectionEnd, 'end');
  } else if (code === 'Tab') {
    TEXTAREA.setRangeText('\t', selectionStart, selectionEnd, 'end');
  } else {
    TEXTAREA.setRangeText(text, selectionStart, selectionEnd, 'end');
  }
}

function handleMouseDown(e) {
  const { target } = e;
  const { innerHTML, classList, dataset } = target;

  if (dataset.code === 'CapsLock') {
    isCapsActive = !isCapsActive;
    if (isCapsActive) {
      changeToShift(true);
    } else {
      changeToShift(false);
    }
  }

  if (dataset.code === 'ShiftLeft' || dataset.code === 'ShiftRight') {
    if (isCapsActive) {
      changeToShift(false);
    } else {
      changeToShift(true);
    }
  }

  classList.add('keyboard__key--pressed');

  printButton(innerHTML, dataset.code);
}

function handleMouseUp(e) {
  const { target } = e;
  const { classList, dataset } = target;

  TEXTAREA.focus();

  if (dataset.code === 'ShiftLeft' || dataset.code === 'ShiftRight') {
    if (isCapsActive) {
      changeToShift(true);
    } else {
      changeToShift(false);
    }
  }

  if (!(target.dataset.code === 'CapsLock' && isCapsActive)) {
    classList.remove('keyboard__key--pressed');
  }
}

// keyboard generation
keyList.forEach((row) => {
  const lang = sessionStorage.getItem('lang');
  const { className, keys } = row;
  const ROW = appendElement(CONTAINER, className);

  keys.forEach((key) => {
    const { className: keyClassName, eng, rus, code } = key;
    const KEY = appendElement(ROW, keyClassName);
    KEY.dataset.code = code;

    KEY.innerHTML = lang === 'eng' ? eng : rus || eng;
    KEY.addEventListener('mousedown', handleMouseDown);
    KEY.addEventListener('mouseup', handleMouseUp);
  });
});

const BUTTONS = document.querySelectorAll('.keyboard__key');

// change language
function changeLanguage(isCapsLock) {
  const lang = sessionStorage.getItem('lang');
  const buttonsArr = [...BUTTONS];

  keyList.forEach((row) => {
    const { keys } = row;

    keys.forEach((key) => {
      const { eng, rus, shiftEng, shiftRus, code } = key;
      const button = buttonsArr.find((b) => b.dataset.code === code);
      if (lang === 'eng') {
        button.innerHTML = isCapsLock && shiftEng ? shiftEng : eng;
      } else {
        button.innerHTML = isCapsLock
          ? shiftRus || shiftEng || eng
          : rus || eng;
      }
    });
  });
}

// shift handle
function changeToShift(isPressed) {
  const lang = sessionStorage.getItem('lang');
  const buttonsArr = [...BUTTONS];

  keyList.forEach((row) => {
    const { keys } = row;

    keys.forEach((key) => {
      const { eng, rus, shiftEng, shiftRus, code } = key;
      const button = buttonsArr.find((b) => b.dataset.code === code);
      if (lang === 'eng') {
        button.innerHTML = isPressed && shiftEng ? shiftEng : eng;
      } else {
        button.innerHTML = isPressed ? shiftRus || shiftEng || eng : rus || eng;
      }
    });
  });
}

// button click handle
function handleButtonDown(event) {
  event.preventDefault();
  TEXTAREA.focus();
  const { code, key, ctrlKey, altKey, shiftKey } = event;

  if (ctrlKey && altKey) {
    const lang = sessionStorage.getItem('lang');
    if (lang === 'eng') {
      sessionStorage.setItem('lang', 'rus');
    } else {
      sessionStorage.setItem('lang', 'eng');
    }
    changeLanguage(isCapsActive);
  }

  if (key === 'CapsLock') {
    isCapsActive = !isCapsActive;
    if (isCapsActive) {
      changeToShift(true);
    } else {
      changeToShift(false);
    }
  }

  if (shiftKey) {
    if (isCapsActive) {
      changeToShift(false);
    } else {
      changeToShift(true);
    }
  }

  const button = [...BUTTONS].find((b) => b.dataset.code === code);
  if (button) {
    button.classList.add('keyboard__key--pressed');
    printButton(button.innerHTML, code);
  }
}

function handleButtonUp(event) {
  const { code, key } = event;

  if (key === 'Shift') {
    if (isCapsActive) {
      changeToShift(true);
    } else {
      changeToShift(false);
    }
  }

  const button = [...BUTTONS].find((b) => b.dataset.code === code);
  if (button && !(button.dataset.code === 'CapsLock' && isCapsActive)) {
    button.classList.remove('keyboard__key--pressed');
  }
}

document.addEventListener('keydown', handleButtonDown);
document.addEventListener('keyup', handleButtonUp);
